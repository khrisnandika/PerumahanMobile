package com.example.perumahan.Model;

public class ModelDisewakan {

    private int id;
    private String gambarRumah, tipeRumah, statusRumah, alamatRumah;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getGambarRumah() {
        return gambarRumah;
    }

    public void setGambarRumah(String gambarRumah) {
        this.gambarRumah = gambarRumah;
    }

    public String getTipeRumah() {
        return tipeRumah;
    }

    public void setTipeRumah(String tipeRumah) {
        this.tipeRumah = tipeRumah;
    }

    public String getStatusRumah() {
        return statusRumah;
    }

    public void setStatusRumah(String statusRumah) {
        this.statusRumah = statusRumah;
    }

    public String getAlamatRumah() {
        return alamatRumah;
    }

    public void setAlamatRumah(String alamatRumah) {
        this.alamatRumah = alamatRumah;
    }
}
